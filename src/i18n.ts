export const fixTypeDescriptions: Record<string, keyof typeof translations.en> = {
  trailing_comma: 'fixTrailingComma',
  single_quotes: 'fixSingleQuotes',
  missing_quotes: 'fixMissingQuotes',
  comments: 'fixComments',
  undefined_values: 'fixUndefined',
  other: 'fixCombined',
  missing_comma: 'fixMissingComma',
  smart: 'fixSmart',
};

export function getFixDescription(locale: Locale, type: string, fixKey?: string): string {
  const key = fixKey === 'smart' ? 'fixSmart' : (fixTypeDescriptions[type] || 'fixCombined');
  const t = translations[locale];
  const val = t[key as keyof typeof t];
  return typeof val === 'function' ? (val as (n: number) => string)(0) : (val as string);
}
